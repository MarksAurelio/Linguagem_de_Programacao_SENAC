""" Imprima os números de 1 a 10. 
for i in range(1, 11):
    print(i)

for: Indica o início de um loop for.
i: É a variável de iteração. Em cada iteração do loop, i receberá o próximo valor da sequência definida 
em range(1, 11).
range(1, 11): A função range() é usada para gerar uma sequência de números. Ela cria uma sequência de 
números começando em 1 (o primeiro argumento) e indo até, mas não incluindo, 11 (o segundo argumento). 
Portanto, esta função gera números de 1 a 10.
print(i): Este é o bloco de código que será executado em cada iteração do loop for. Aqui, estamos 
simplesmente imprimindo o valor atual de i."""
for i in range (1, 11):
    print(i)
